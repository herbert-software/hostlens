"""Snapshot tests for the security + package os-shell builtin inspectors.

`add-security-baseline-and-package-inspectors` (os-shell wave-2):
`security.failed_logins`, `security.sudo_history`,
`security.world_writable_dirs`, `pkg.pending_updates`,
`pkg.security_patches`, `pkg.held_back`.

These run the **real** `InspectorRunner` against `ReplayTarget` fixtures (see
`tests/inspectors/fixtures/security/` + `fixtures/pkg/`, generated by
`_record_security_pkg.py`), exercising the full
`preflight → render → collect → parse → findings` path **offline** — zero real
host, zero journal, zero apt/dnf.

The two sampling_window inspectors (`failed_logins` / `sudo_history`) are run
with the SAME frozen clock the recorder used (`FROZEN_DT`) so the rendered
`--since` timestamps match the recorded command byte-for-byte. Every test
asserts `replay.misses == []` so any drift between the rendered command and the
recorded fixture fails loud.

Coverage per inspector (D-5): a finding-trigger fixture (asserts a finding fires
— proving detection, no no-op), an ok fixture (zero findings), and an
**exception** fixture that models the collector's fail-loud path (empty stdout +
non-zero exit → `status=exception`). The exception tests are the spec
false-negative guards (os-shell-inspector-suite「fail-loud 不假阴」/「pkg 无包
管理器非 ok」): a data-source / package-manager failure MUST surface as
`status=exception`, never a silent `status=ok` with a zero count.
"""

from __future__ import annotations

from datetime import UTC, datetime
from pathlib import Path

import pytest
import structlog

from hostlens.core.config import Settings
from hostlens.inspectors.loader import load_manifest
from hostlens.inspectors.result import InspectorResult
from hostlens.inspectors.runner import InspectorRunner
from hostlens.targets.registry import TargetRegistry
from hostlens.targets.replay import ReplayTarget

_BUILTIN_ROOT = Path(__file__).resolve().parents[2] / "src" / "hostlens" / "inspectors" / "builtin"
_FIXTURE_ROOT = Path(__file__).resolve().parent / "fixtures"

# Must match `_record_security_pkg.FROZEN_DT` so the sampling_window commands
# render identically to the recorded fixtures.
FROZEN_DT = datetime(2026, 1, 2, 12, 0, 0, tzinfo=UTC)


def _runner() -> InspectorRunner:
    return InspectorRunner(
        TargetRegistry(),
        settings=Settings(),
        logger=structlog.get_logger("test"),
        clock=lambda: FROZEN_DT,
    )


async def _run(domain: str, inspector: str, fixture: str) -> tuple[ReplayTarget, InspectorResult]:
    manifest = load_manifest(_BUILTIN_ROOT / domain / f"{inspector}.yaml")
    replay = ReplayTarget("os-host", fixture=_FIXTURE_ROOT / domain / fixture)
    result = await _runner().run(manifest, replay)
    return replay, result


# --------------------------------------------------------------------------- #
# security.failed_logins
# --------------------------------------------------------------------------- #


async def test_failed_logins_burst_detected() -> None:
    replay, result = await _run("security", "failed_logins", "failed_logins_burst.json")
    assert replay.misses == []
    assert result.status == "ok"
    assert result.output == {"failed": 25}
    assert len(result.findings) == 1
    assert result.findings[0].severity == "warning"


def test_failed_logins_double_unit_command_locked() -> None:
    # The cross-distro unit OR (D-6.5) is locked at the command-string level:
    # the recorded main command lists BOTH ssh.service + sshd.service so a
    # RHEL-family host (sshd.service) cannot silently report 0 failures. We
    # assert on the recorded fixture command (the byte-level lock), since the
    # replay path itself does not re-run journalctl's OR semantics.
    import json

    fixture = json.loads(
        (_FIXTURE_ROOT / "security" / "failed_logins_burst.json").read_text(encoding="utf-8")
    )
    main = next(c["cmd"] for c in fixture["commands"] if "--since" in c["cmd"])
    assert "_SYSTEMD_UNIT=ssh.service" in main
    assert "_SYSTEMD_UNIT=sshd.service" in main
    assert "TZ=UTC" in main


async def test_failed_logins_ok_no_findings() -> None:
    replay, result = await _run("security", "failed_logins", "failed_logins_ok.json")
    assert replay.misses == []
    assert result.status == "ok"
    assert result.findings == []


async def test_failed_logins_unreachable_is_exception_not_false_negative() -> None:
    replay, result = await _run("security", "failed_logins", "failed_logins_unreachable.json")
    assert replay.misses == []
    # journal unreadable → fail-loud → exception, NOT status=ok failed=0.
    assert result.status == "exception"
    assert result.findings == []



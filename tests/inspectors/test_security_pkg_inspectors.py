@pytest.mark.parametrize(
    ("domain", "inspector", "fixture"),
    [
        ("security", "failed_logins", "failed_logins_unreachable.json"),
        ("security", "sudo_history", "sudo_history_unreachable.json"),
        ("security", "world_writable_dirs", "world_writable_dirs_unreachable.json"),
        ("pkg", "pending_updates", "pending_updates_no_pkg_mgr.json"),
        ("pkg", "security_patches", "security_patches_failure.json"),
        ("pkg", "held_back", "held_back_failure.json"),
    ],
)
async def test_fail_loud_never_silent_ok(domain: str, inspector: str, fixture: str) -> None:
    # spec os-shell-inspector-suite fail-loud guard, swept across all 6: a
    # collection failure MUST be status=exception, never status=ok.
    _, result = await _run(domain, inspector, fixture)
    assert result.status == "exception", f"{inspector} swallowed a collection failure as ok"

# --------------------------------------------------------------------------- #


async def test_exception_burst_detected() -> None:
    replay, result = await _run(
        "log",
        "exception_burst",
        "exception_burst_burst.json",
        parameters={"log_path": "/var/log/app.log"},
    )

    assert replay.misses == []
    assert result.status == "ok"
    assert result.output == {
        "results": [
            {"signature": "java.lang.NullPointerException", "count": 42},
            {"signature": "java.lang.OutOfMemoryError", "count": 3},
        ],
    }
    assert [(f.severity, f.message) for f in result.findings] == [
        ("warning", "Exception burst: java.lang.NullPointerException occurred 42 times"),
    ]


async def test_exception_burst_ok_no_findings() -> None:
    replay, result = await _run(
        "log",
        "exception_burst",
        "exception_burst_ok.json",
        parameters={"log_path": "/var/log/app.log"},
    )

    assert replay.misses == []
    assert result.status == "ok"
    assert result.findings == []

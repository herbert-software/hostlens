"""Snapshot tests for the wave-1 network / DNS / NTP builtin inspectors.

`add-os-shell-inspectors-wave1` group 3: `net.connections`,
`net.listening_ports`, `net.dns.resolve`, `net.ntp.drift`.

These run the **real** `InspectorRunner` against `ReplayTarget` fixtures (see
`tests/inspectors/fixtures/os_net/`, generated by `_record_os_net.py`), so they
exercise the full `preflight → render → collect → parse → findings` path
**offline** — zero real host, zero `ss`/`dig`/`chronyc`. The probes are
Linux-only; the recorder crafts the JSON each collector pipeline would emit per
scenario.

Each inspector has BOTH an abnormal-scenario fixture (asserts a finding fires —
proving it can detect the target fault, no no-op) and an ok-scenario fixture
(asserts zero findings). `net.dns.resolve` additionally has an injection-safety
test: a malicious `names` payload is rejected by the parameter `pattern` BEFORE
any command renders (no exec ever happens). Every replay test asserts
`replay.misses == []` so a drift between rendered command and recorded fixture
fails loud.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, ClassVar

import structlog

from hostlens.core.config import Settings
from hostlens.inspectors.loader import load_manifest
from hostlens.inspectors.result import InspectorResult
from hostlens.inspectors.runner import InspectorRunner
from hostlens.targets.base import Capability, ExecResult
from hostlens.targets.registry import TargetRegistry
from hostlens.targets.replay import ReplayTarget

_BUILTIN_DIR = (
    Path(__file__).resolve().parents[2] / "src" / "hostlens" / "inspectors" / "builtin" / "net"
)
_FIXTURE_DIR = Path(__file__).resolve().parent / "fixtures" / "os_net"


def _logger() -> structlog.stdlib.BoundLogger:
    return structlog.get_logger("test")  # type: ignore[no-any-return]


def _runner() -> InspectorRunner:
    return InspectorRunner(TargetRegistry(), settings=Settings(), logger=_logger())


async def _run(
    inspector: str,
    fixture: str,
    parameters: dict[str, Any] | None = None,
) -> tuple[ReplayTarget, InspectorResult]:
    manifest = load_manifest(_BUILTIN_DIR / f"{inspector}.yaml")
    replay = ReplayTarget("os-host", fixture=_FIXTURE_DIR / fixture)
    result = await _runner().run(manifest, replay, parameters=parameters)
    return replay, result



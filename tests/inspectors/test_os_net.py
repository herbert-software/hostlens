async def test_dns_resolve_injection_safe_renders_quoted() -> None:
    """A benign name renders into a `shlex.quote`-wrapped loop word.

    Captured at record time the rendered command embeds the name as a single
    quoted word (`for nm in safe-host.example`), proving the `| map('sh')`
    filter is on the interpolation path. Replay re-renders the same command
    and must hit the recorded fixture byte-for-byte.
    """

    replay, result = await _run(
        "dns_resolve",
        "dns_resolve_injection_safe.json",
        parameters={"names": ["safe-host.example"]},
    )

    assert replay.misses == []
    assert result.status == "ok"
    assert result.findings == []
    # The recorded collector command must carry the name through shlex.quote /
    # the `| map('sh')` filter (here a no-special-char name needs no quoting,
    # but the word must appear exactly once in the rendered loop header). Read
    # the fixture JSON directly — ReplayTarget keys commands by SHA256.
    fixture = json.loads(
        (_FIXTURE_DIR / "dns_resolve_injection_safe.json").read_text(encoding="utf-8")
    )
    dns_cmd = next(c["cmd"] for c in fixture["commands"] if "dig +short" in c["cmd"])
    assert "for nm in safe-host.example;" in dns_cmd


class _ProbeOnlyTarget:
    """Target that answers preflight probes but rejects the main collector cmd.

    Used by the injection-payload test. The runner's preflight (binary `command
    -v X` / file `[ -r P ]` probes, step 5) runs BEFORE parameter validation
    (step 7), so those probes legitimately reach `exec`. But the rendered
    collector command — the only place the malicious `names` element could land
    in a shell-evaluated position — must NEVER run, because parameter validation
    rejects the payload first. Any non-probe `exec` therefore fails loud.
    """

    type = "local"
    name = "probe-only-host"
    capabilities: ClassVar[set[Capability]] = {Capability.SHELL}

    async def exec(
        self,
        cmd: str,
        *,
        timeout: int,
        env: dict[str, str] | None = None,
    ) -> ExecResult:
        del timeout, env
        if cmd.startswith("command -v "):
            binary = cmd[len("command -v ") :].strip().strip("'\"")
            return ExecResult(
                exit_code=0,
                stdout=f"/usr/bin/{binary}\n",
                stderr="",
                duration_seconds=0.0,
                timed_out=False,
            )
        if cmd.startswith("[ -r "):
            return ExecResult(
                exit_code=0, stdout="", stderr="", duration_seconds=0.0, timed_out=False
            )
        raise AssertionError(
            f"collector command must not be reached for a rejected payload: {cmd!r}"
        )

    async def read_file(self, path: str) -> bytes:
        raise AssertionError(f"read_file must not be reached: {path!r}")


async def test_dns_resolve_injection_payload_rejected_before_command() -> None:
    """A shell-injection payload in `names` is rejected by the schema pattern.

    The payload `'; whoami; #` cannot satisfy `^[a-zA-Z0-9.-]+$`, so the runner
    short-circuits to `status=exception` (parameter_validation_failed) before
    the collector command is rendered or run — the malicious string never
    reaches a shell-evaluated position (`_ProbeOnlyTarget` asserts the main
    command is never exec'd).
    """

    manifest = load_manifest(_BUILTIN_DIR / "dns_resolve.yaml")
    target = _ProbeOnlyTarget()
    result = await _runner().run(
        manifest,
        target,  # type: ignore[arg-type]
        parameters={"names": ["'; whoami; #"]},
    )

    assert result.status == "exception"
    assert result.error is not None
    assert result.error.startswith("parameter_validation_failed")
    assert result.findings == []



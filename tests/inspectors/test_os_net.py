async def test_connections_close_wait_leak_detected() -> None:
    replay, result = await _run("connections", "connections_close_wait_leak.json")

    assert replay.misses == []
    assert result.status == "ok"
    assert result.output == {
        "total": 1234,
        "established": 420,
        "time_wait": 150,
        "close_wait": 512,
        "syn_sent": 2,
        "syn_recv": 1,
        "fin_wait": 3,
        "listen": 45,
    }
    assert [(f.severity, f.message) for f in result.findings] == [
        (
            "critical",
            "CLOSE_WAIT backlog of 512 sockets; application is leaking unclosed connections",
        ),
    ]


async def test_connections_ok_no_findings() -> None:
    replay, result = await _run("connections", "connections_ok.json")

    assert replay.misses == []
    assert result.status == "ok"
    assert result.findings == []


